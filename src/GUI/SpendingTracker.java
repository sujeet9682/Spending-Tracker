    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            java.util.Date dt = d.getDate();
            String s1 = a.getText();
            String categ = (String)category.getSelectedItem();

            if(dt!=null && !s1.equals("") && !categ.equals("") ){
                int amount = Integer.parseInt(s1);
                java.sql.Date date = new java.sql.Date(dt.getTime());
                db.dbConnect.s.executeUpdate("insert into spendings(category,sdate,amount) values('"+categ+"', '"+date+"', '"+s1+"')");
                JOptionPane.showMessageDialog(null, "Expense Added Successfully");
                getEntries();

            }else{
                JOptionPane.showMessageDialog(null, "Please enter all entries");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

