    private void getEntries(){
		try{
                    javax.swing.table.DefaultTableModel dtm =(javax.swing.table.DefaultTableModel)jTable1.getModel();
                    int rc=dtm.getRowCount();
                    while(rc--!=0){
                        dtm.removeRow(0);
                    }
                    java.time.LocalDate cd=java.time.LocalDate.now();
                    java.time.LocalDate bd=cd.minusDays(20);
                    ResultSet rs=db.dbConnect.s.executeQuery("select * from spendings where sdate<= '"+cd+"' and sdate>='"+bd+"' order by sdate desc");
                    int total=0;
                    while(rs.next()){
                        int t=rs.getInt("amount");
                        total+=t;
                        Object o[]={rs.getInt("sid"),rs.getString("category"),rs.getDate("sDate"),rs.getInt("amount")};
                                dtm.addRow(o);
                    }
                    jLabel6.setText(total+"");
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null, ex);
                }
                        
                        }
    private void displayCategory(){
         try{
             category.removeAllItems();
            ResultSet rs = db.dbConnect.s.executeQuery("select * from category_info");
            while(rs.next()){
                category.addItem(rs.getString("category"));
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
